# stack := 

# A stack is a linear data structure that stores items in a Last-In/First-Out (LIFO) 
# or First-In/Last-Out (FILO) manner. In stack, a new element is added at one end and 
# an element is removed from that end only. The insert and delete operations are often 
# called push and pop.

# push(a) – Inserts the element ‘a’ at the top of the stack – Time Complexity: O(1)
# pop() – Deletes the topmost element of the stack – Time Complexity: O(1)
# isempty() – Returns whether the stack is empty – Time Complexity: O(1)
# isFull() - Returns whether the stack is full